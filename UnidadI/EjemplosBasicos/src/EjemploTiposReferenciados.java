
public class EjemploTiposReferenciados {
	public static void main(String[] args) {
		String nombre = new String("Juan");
		String apellido = "Perez";
		System.out.println("\"Hola " + nombre + " " + apellido+"\"");
		System.out.println("\'Hola " + nombre + " " + apellido+"\'");
		System.out.println("\\");
	}
}
